#include <stdio.h>

int        match(char *s1, char *s2)
{

	int f;
	int s;

	f = 0;
	s = 0;

	if (s1[f] != '\0' && s2[s] != '\0')
		return (0);
	while (s1[f] != '\0' && s2[s] != '\0')
	  {
		if (s2[s] == '*')
		  {
			while (s2[s] == '*' || s2[s] == s2[s + 1])
				s++;
		  }
	while (s1[f] != s2[s] || s1[f] == (s1[f + 1]))
	{
		if (s1[f] == '\0' && s2[s] != '\0')
		{

			f++;
		}
		else if (s1[f] != s2[s] && s2[s] != '\0')
			return 0;
		else if (s1[f] == s2[s])
		{
			f++;
			s++;
		}
	}

	}
	return (1);
	  
}


int main ()
{
  char s1[] = "hello.c";
  char s2[] = "************lo.c";

  printf("%d", match(s1, s2));
  return 0;
}
